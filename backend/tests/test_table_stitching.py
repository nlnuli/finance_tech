COLUMN_BOXES = ((0.05, 0.52), (0.60, 0.75), (0.80, 0.95))


def make_row(values: list[str], top: float, row_index: int = 0) -> TableRow:
    return TableRow(
        cells=[
            TableCell(
                text=value,
                row_index=row_index,
                column_index=column_index,
                bounding_box=BoundingBox(
                    left=left,
                    top=top,
                    right=right,
                    bottom=top + 0.03,
                ),
            )
            for column_index, (value, (left, right)) in enumerate(
                zip(values, COLUMN_BOXES, strict=True)
            )
        ]
    )


def make_table(
    table_id: str,
    page_number: int,
    title: str,
    top: float,
    bottom: float,
    labels: list[str],
    headers: list[str] | None = None,
) -> DocumentTable:
    header_rows = [make_row(headers, top)] if headers else []
    body_rows = [
        make_row(
            [label, str(100 + index), str(90 + index)],
            top + 0.08 + index * 0.04,
            index + 1,
        )
        for index, label in enumerate(labels)
    ]
    return DocumentTable(
        id=table_id,
        page_number=page_number,
        title=title,
        header_rows=header_rows,
        body_rows=body_rows,
        bounding_box=BoundingBox(left=0.05, top=top, right=0.95, bottom=bottom),
        source_processors=["form"],
        source_refs=[table_id],
    )


