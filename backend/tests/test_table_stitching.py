class LogicalTableChunkingTests(unittest.TestCase):
    def test_cross_page_chunk_contains_provenance_and_repeated_header(self):
        first = make_table(
            "first",
            1,
            "Balance Sheets",
            0.45,
            0.95,
            ["Current assets", "Total current assets"],
            ["Item", "2025", "2024"],
        )
        second = make_table(
            "second",
            2,
            "Balance Sheets",
            0.05,
            0.60,
            ["Non-current assets", "Total assets"],
            ["Item", "2025", "2024"],
        )
        stitching = stitch_tables([first, second], [], minimum_score=0.75)
        document = UnifiedDocument(
            file_id=99,
            filename="report.pdf",
            page_count=2,
            tables=[first, second],
        )

        chunks = build_document_chunks(
            document,
            "default",
            "user-1",
            chunk_size=180,
            logical_tables=stitching.logical_tables,
        )
        table_chunks = [
            chunk for chunk in chunks if chunk["metadata"]["content_type"] == "table"
        ]

        self.assertGreater(len(table_chunks), 1)
        for chunk in table_chunks:
            metadata = chunk["metadata"]
            self.assertIn("Item", chunk["content"])
            self.assertIn("页码：1-2", chunk["content"])
            self.assertTrue(metadata["is_cross_page"])
            self.assertEqual(metadata["source_table_ids"], ["first", "second"])
            self.assertEqual((metadata["page_start"], metadata["page_end"]), (1, 2))
            self.assertTrue(metadata["row_source_pages"])

    def test_single_page_logical_table_keeps_physical_id(self):
        table = make_table(
            "single",
            1,
            "Financial metrics",
            0.20,
            0.60,
            ["Revenue"],
            ["Item", "2025", "2024"],
        )
        stitching = stitch_tables([table], [], minimum_score=0.75)
        document = UnifiedDocument(
            file_id=3,
            filename="report.pdf",
            page_count=1,
            tables=[table],
        )

        chunks = build_document_chunks(
            document,
            "default",
            "user-1",
            logical_tables=stitching.logical_tables,
        )

        table_chunk = next(
            chunk for chunk in chunks if chunk["metadata"]["content_type"] == "table"
        )
        self.assertEqual(table_chunk["metadata"]["source_id"], "single")
        self.assertEqual(
            table_chunk["metadata"]["chunk_id"],
            "file-3-table-single-0",
        )


