    def test_builds_three_page_chain_without_branching(self):
        tables = [
            make_table(
                f"cash-{page}",
                page,
                "Statements of Cash Flows",
                0.45 if page == 1 else 0.05,
                0.95,
                [f"Operating activities {page}", "Cash equivalents"],
                ["Item", "2025", "2024"],
            )
            for page in (1, 2, 3)
        ]

        result = stitch_tables(tables, [], minimum_score=0.75)

        self.assertEqual(result.logical_table_count, 1)
        self.assertEqual(
            result.logical_tables[0].source_table_ids, ["cash-1", "cash-2", "cash-3"]
        )
        self.assertEqual(sum(decision.matched for decision in result.decisions), 2)

