    def test_merges_adjacent_financial_table_with_repeated_header(self):
        first = make_table(
            "p15-assets",
            15,
            "Balance Sheets",
            0.45,
            0.96,
            ["Current assets", "Total current assets"],
            ["Item", "2025", "2024"],
        )
        second = make_table(
            "p16-assets",
            16,
            "Balance Sheets",
            0.04,
            0.60,
            ["Non-current assets", "Total assets"],
            ["Item", "2025", "2024"],
        )

        result = stitch_tables([first, second], [], minimum_score=0.75)

        self.assertEqual(result.logical_table_count, 1)
        self.assertEqual(result.stitched_table_count, 1)
        logical = result.logical_tables[0]
        self.assertEqual((logical.page_start, logical.page_end), (15, 16))
        self.assertEqual(logical.source_table_ids, ["p15-assets", "p16-assets"])
        self.assertEqual(
            [row.source_page_number for row in logical.body_rows],
            [15, 15, 16, 16],
        )
        self.assertTrue(result.decisions[0].matched)

